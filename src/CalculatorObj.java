public class CalculatorObj {
    int sum(int a, int b){
        int sum=a + b;
        return sum;
    }
    int mul(int a, int b){
        int mul= a*b;
        return mul;
    }
    int sub(int a, int b){
        int sub=a-b;
        return sub;
    }

    int div(int a, int b) {
        int abc=a/b;
        return abc;
    }





}
